package com.aliyun.bean;

public class MemDetail {
	private String time;
	private String used;
	private String buff;
	private String cach;
	private String free;
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getUsed() {
		return used;
	}
	public void setUsed(String used) {
		this.used = used;
	}
	public String getBuff() {
		return buff;
	}
	public void setBuff(String buff) {
		this.buff = buff;
	}
	public String getCach() {
		return cach;
	}
	public void setCach(String cach) {
		this.cach = cach;
	}
	public String getFree() {
		return free;
	}
	public void setFree(String free) {
		this.free = free;
	}
	
}
